def gerar_dados_treinamento(
    qtd_exemplos=30,
    funcao="fugir",
    perigo=1,
    oportunidade=0,
    neutro=0,
    player_index=1,
    limite_distancia=12,          # m√°ximo para gera√ß√£o
    distancia_perto=5,            # novo par√¢metro para considerar "perto"
    limite_powerup=12,
    bomba_existe=1,
    bomba_player=1,
    limite_min_distancia=1,
    limite_min_powerup=1
):
    """
    Gera dados sint√©ticos controlados apenas com dist√¢ncias, para treinar IA.

    - `limite_distancia`: dist√¢ncia m√°xima poss√≠vel de gera√ß√£o aleat√≥ria.
    - `distancia_perto`: dist√¢ncia usada para marcar se h√° jogadores pr√≥ximos.
    """

    if player_index not in [1, 2, 3, 4]:
        raise ValueError("player_index deve ser um n√∫mero entre 1 e 4.")

    dados = []

    for _ in range(qtd_exemplos):
        # --- DIST√ÇNCIAS DOS JOGADORES ---
        dist_jogadores = {}
        jogadores_perto = 0

        for i in range(1, 5):
            if i == player_index:
                dist_jogadores[i] = 0
                continue

            if perigo:
                min_val = limite_min_distancia
                max_val = limite_distancia
            else:
                min_val = limite_distancia + 1
                max_val = limite_distancia * 2

            dist = random.randint(min_val, max_val)
            dist_jogadores[i] = dist

            # Agora usa o novo par√¢metro para verificar proximidade
            if dist <= distancia_perto:
                jogadores_perto += 1

        mais_de_um_jogador_perto = 1 if jogadores_perto > 1 else 0

        # --- DIST√ÇNCIAS DAS BOMBAS ---
        dist_bombas = {}

        if bomba_existe:
            for i in range(1, 5):
                if i == player_index:
                    if bomba_player == 1:
                        min_b = limite_min_distancia
                        max_b = min(3, limite_distancia)
                        max_b = max(min_b, max_b)
                        dist_bombas[i] = random.randint(min_b, max_b)
                    else:
                        dist_bombas[i] = 0
                else:
                    dist_jog = dist_jogadores[i]
                    max_v = limite_distancia * 2
                    min_v = limite_min_distancia

                    nova_dist = dist_jog + random.randint(-2, 2)
                    nova_dist = max(min_v, min(nova_dist, max_v))
                    dist_bombas[i] = nova_dist
        else:
            for i in range(1, 5):
                dist_bombas[i] = 0

        # --- POWERUP ---
        powerup_existe = random.choice([0, 1])
        if powerup_existe:
            min_pu = limite_min_powerup
            max_pu = limite_powerup
            max_pu = max(min_pu, max_pu)
            dist_powerup = random.randint(min_pu, max_pu)
        else:
            dist_powerup = 0

        player_com_powerup = random.choice([0, 1])

        linha = {
            "dist_jogador1": dist_jogadores[1],
            "dist_jogador2": dist_jogadores[2],
            "dist_jogador3": dist_jogadores[3],
            "dist_jogador4": dist_jogadores[4],
            "dist_bomba1": dist_bombas[1],
            "dist_bomba2": dist_bombas[2],
            "dist_bomba3": dist_bombas[3],
            "dist_bomba4": dist_bombas[4],
            "bomba_player": bomba_player,
            "bomba_existe": bomba_existe,
            "powerup_existe": powerup_existe,
            "dist_powerup": dist_powerup,
            "player_com_powerup": player_com_powerup,
            "perigo": perigo,
            "mais_de_um_jogador_perto": mais_de_um_jogador_perto,
            "oportunidade": oportunidade,
            "neutro": neutro,
            "funcao": funcao
        }

        dados.append(linha)

    print("\n--- Dados gerados com sucesso ‚úÖ ---\n")
    return dados



    


